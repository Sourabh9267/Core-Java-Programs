	// A Java Program demonstrating the conversion of a primitive integer (`int`) to its corresponding wrapper class (`Integer`) using the `valueOf` method and then printing both values.
	class ExWrapper
	{
		
		
		
		public static void main(String []args)
		{
				
				
				int a=100;
				
				Integer i=Integer.valueOf(a);
				
				System.out.println(a+" "+i);
			
			
			
		}
		
		
		
		
		
		
		
		
	}