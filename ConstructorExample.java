// Simple Default Constructor Program//
// A Java Program demonstrating the use of a default constructor to initialize an instance variable and display its value.

class ConstructorExample
{

	int x;
	
	ConstructorExample()
	{
	
		x=100;
	
	System.out.println("Value is : "+x);
	
	}


	public static void main(String []args)
	{
		
		ConstructorExample ob=new ConstructorExample();
	
	}



}